    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ulesSzam = (int) jSpinner1.getValue();
        String marka = jComboBox1.getSelectedItem().toString();
        String evjarat = jComboBox2.getSelectedItem().toString();
        String ules = jSpinner1.getValue().toString();
        String km = jComboBox3.getSelectedItem().toString();
        String nev = jTextField1.getText();
        String email = jTextField3.getText();
        String cim = jTextField4.getText();
         if (ulesSzam > 5) {
            // Show an error message in a pop-up dialog
            JOptionPane.showMessageDialog(this, "Hibás ülés érték, max. 5 ülést lehet beállítani", "Hiba", JOptionPane.ERROR_MESSAGE);
        }else {
            jTextArea1.setText("Amiket kijelöljt: "+ "\n" + "+Márka: " + marka + "\n" + "Évjárat: " + evjarat + "\n" + "Ülés: " + ules + "\n"+ "Km: " + km);
       }
       if (nev.equals("")){
           JOptionPane.showMessageDialog(rootPane, " Kérem adja meg a nevét");
       }
       if (email.equals("")){
           JOptionPane.showMessageDialog(rootPane, " Kérem adja meg az email címét");
       }
       if (cim.equals("")){
           JOptionPane.showMessageDialog(rootPane, " Kérem adja meg a címét");
       }
           
    }//GEN-LAST:event_jButton1ActionPerformed

